class World():
    def __init__(self, data):
        self.tile_list = []

        snow = pygame.image.load('ice/Tiles/tundra.png')
        another = pygame.image.load('candy/Tiles/cake.png')

        row_count = 0
        for row in data:
            count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(another, (tile_size, tile_size))
                    rect = img.get_rect()
                    rect.x = count * tile_size
                    rect.y = row_count * tile_size
                    tile = (img, rect)
                    self.tile_list.append(tile)
                if tile == 2:
                    img = pygame.transform.scale(another, (tile_size, tile_size))
                    rect = img.get_rect()
                    rect.x = count * tile_size
                    rect.y = row_count * tile_size
                    tile = (img, rect)
                    self.tile_list.append(tile)
                count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])


